# Filtro S/ Comprehension

numbers = [1, 30, 21, 2, 9, 65, 34]
pairs = []

for number in numbers:
    if number % 2 == 0:
        pairs.append

# LIST COMPREHENSION

numbers = [1, 30, 21, 2, 9, 65, 34]
pairs = [number for number in numbers if number % 2 == 0]
